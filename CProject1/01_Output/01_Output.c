// 소스코드
// 컴퓨터에게 명령을 내리는 문장입니다.
// 주석 처리가 되지 않는 모든 문장은 컴퓨터가 명령이라 인식합니다.

// include : 포함하다
// std : Standard(표준의)
// i   : Input   (입력)
// o   : Output  (출력)
// #   : 프로그램의 실행 전에 미리 실행시킬 명령입니다.
#include <stdio.h>   // C 언어 표준의 입출력 파일 포함시켜라

// main : 프로그램의 시작 지점을 결정합니다.
int main()
{
	// {} (code block) : 실행 시킬 명령을 작성할 공간입니다.
	
	printf("Hello World !");
	
	// {}, # 이 포함된 명령은 작성하지 않습니다.

	return 0; // return : 프로그램을 종료합니다.
}
